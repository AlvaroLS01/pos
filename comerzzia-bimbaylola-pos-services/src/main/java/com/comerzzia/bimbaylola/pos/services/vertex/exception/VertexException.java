package com.comerzzia.bimbaylola.pos.services.vertex.exception;


public class VertexException extends Exception{

	/**
	 *
	 */
	private static final long serialVersionUID = 592458523573906140L;

	public VertexException() {
		super();
	}

	public VertexException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public VertexException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VertexException(String arg0) {
		super(arg0);
	}

	public VertexException(Throwable arg0) {
		super(arg0);
	}
	
	

}
