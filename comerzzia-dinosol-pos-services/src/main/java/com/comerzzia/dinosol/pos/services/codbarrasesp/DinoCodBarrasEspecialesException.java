package com.comerzzia.dinosol.pos.services.codbarrasesp;

public class DinoCodBarrasEspecialesException extends Exception {

	private static final long serialVersionUID = 4307053169255393490L;

	public DinoCodBarrasEspecialesException() {
		super();
	}

	public DinoCodBarrasEspecialesException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DinoCodBarrasEspecialesException(String message, Throwable cause) {
		super(message, cause);
	}

	public DinoCodBarrasEspecialesException(String message) {
		super(message);
	}

	public DinoCodBarrasEspecialesException(Throwable cause) {
		super(cause);
	}

}
