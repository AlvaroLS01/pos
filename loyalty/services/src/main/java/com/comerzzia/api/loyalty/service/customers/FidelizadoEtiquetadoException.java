package com.comerzzia.api.loyalty.service.customers;

public class FidelizadoEtiquetadoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1331997518675965845L;

	public FidelizadoEtiquetadoException() {
		super();
	}

	public FidelizadoEtiquetadoException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FidelizadoEtiquetadoException(String message, Throwable cause) {
		super(message, cause);
	}

	public FidelizadoEtiquetadoException(String message) {
		super(message);
	}

	public FidelizadoEtiquetadoException(Throwable cause) {
		super(cause);
	}
	
	
}
